/// <summary>
/// Represents the status of a quest.
/// </summary>
public enum QuestStatus
{
    /// <summary>
    /// The quest hasn't been started.
    /// </summary>
    NotStarted,

    /// <summary>
    /// The quest has been started, but isn't yet complete.
    /// </summary>
    InProgress,

    /// <summary>
    /// The quest has been completed.
    /// </summary>
    Completed
}
