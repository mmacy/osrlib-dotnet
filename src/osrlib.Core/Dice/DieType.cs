/// <summary>
/// Specifies the number of sides of a <see cref="osrlib.Dice.Die"/>.
/// </summary>
public enum DieType
{
    /// <summary>Single-sided die.</summary>
    d1 = 1,
    /// <summary>Two-sided die.</summary>
    d2 = 2,
    /// <summary>Four-sided die.</summary>
    d4 = 4,
    /// <summary>Six-sided die.</summary>
    d6 = 6,
    /// <summary>Eight-sided die.</summary>
    d8 = 8,
    /// <summary>Ten-sided die.</summary>
    d10 = 10,
    /// <summary>Twelve-sided die.</summary>
    d12 = 12,
    /// <summary>Twenty-sided die.</summary>
    d20 = 20,
    /// <summary>Percentage die.</summary>
    d100 = 100
}